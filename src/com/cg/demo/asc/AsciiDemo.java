package com.cg.demo.asc;

public class AsciiDemo {

	public static void main(String[] args) {

		
		for (int i =1 ; i <=256; i++) {
			char ch = (char) i;
			System.out.println(i+ "-" +ch);
		}
		
		for ( int j=1; j<=256;j++) {
			char c = (char) j;
			System.out.println(j+" "+ c);
		}
		
		
	}

}


